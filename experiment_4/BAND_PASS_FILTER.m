clc;clear all;close all;
wc1=0.25*pi;
wc2=0.75*pi;
N=25;
e=0.001;
a=(N-1)/2;
n=0:1:N-1;
hd=(sin(wc2*(n-a+e))-sin(wc1*(n-a+e)))./(pi*(n-a+e));

wr=boxcar(N);
hn=hd.*wr';
w=0:0.01:pi;
h=freqz(hn,1,w);
subplot(2,1,1);
plot(w/pi,abs(h));
grid;
xlabel('normalized frequency');
ylabel('magnitude');
title('BAND PASS FILTER using Rectangular window');

wh=hamming(N);
hn=hd.*wh';
w=0:0.01:pi;
h=freqz(hn,1,w);
subplot(2,1,2);
plot(w/pi,abs(h));
grid;
xlabel('normalized frequency');
ylabel('magnitude');
title('BAND PASS FILTER using Hamming window');
