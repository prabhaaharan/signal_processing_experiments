clc;clear all;close all;
wc=0.5*pi;
N=11;
e=0.001;
n=-(N-1)/2:1:(N-1)/2;
hd1=(sin(pi*(n+e))-sin(wc*(n+e)))./(pi*(n+e));
wr=boxcar(N);
hm1=hd1.*wr';
w=0:0.01:pi;
h1=freqz(hm1,1,w);
%subplot(2,2,1);
plot(w/pi,abs(h1),'-');
grid;
xlabel('normalised frequency');
ylabel('magniutde');
title('FIR HIGH pass filter magnitude using rectangular window');
subplot(2,2,2);
plot(w/pi,angle(h1),'-');
grid;
xlabel('normalised frequency');
ylabel('phase angle');
title('FIR HIGH pass filter phase using rectangular window');

wh=hamming(N);
hm2=hd1.*wh';
h2=freqz(hm2,1,w);
subplot(2,2,3);
plot(w/pi,abs(h2),'-');
grid;
xlabel('non frequency');
ylabel('phase angle');
title('FIR HIGH pass filter magnitude using hamming window');
subplot(2,2,4);
plot(w/pi,angle(h2),'-');
grid;
xlabel('normalised frequency');
ylabel('phase angle');
title('FIR HIGH pass filter phase using hamming window');